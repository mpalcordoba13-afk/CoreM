void fb_draw_char(int x,int y,unsigned char c,uint32_t fg,uint32_t bg) {
    if(c<32||c>127) c='?';
    const uint8_t *g=font8x8[c-32];
    for(int row=0;row<8;row++) {
        uint8_t byte = g[row];
        for(int col=0;col<8;col++) {
            if (byte & (0x01 << col)) {
                fb_put_pixel(x + col, y + row, fg);
            } else {
                fb_put_pixel(x + col, y + row, bg);
            }
        }
    }
}

void fb_draw_str(int x,int y,const char *s,uint32_t fg,uint32_t bg) {
    int cx=x;
    while(*s) {
        if(*s=='\n'){cx=x;y+=11;}
        else{fb_draw_char(cx,y,*s,fg,bg);cx+=9;}
        s++;
    }
}

void fb_draw_int(int x,int y,int n,uint32_t fg,uint32_t bg) {
    char buf[12]; int i=0;
    if(n==0){buf[i++]='0';}
    else{
        if(n<0){buf[i++]='-';n=-n;}
        char tmp[10];int j=0;
        while(n>0){tmp[j++]='0'+n%10;n/=10;}
        while(j>0)buf[i++]=tmp[--j];
    }
    buf[i]='\0';
    fb_draw_str(x,y,buf,fg,bg);
}

/* Texto escalado (para logos grandes) */
void fb_draw_char_scaled(int x,int y,unsigned char c,uint32_t fg,uint32_t bg,int scale) {
    if(c<32||c>127) c='?';
    const uint8_t *g=font8x8[c-32];
    for(int row=0;row<8;row++) {
        uint8_t byte = g[row];
        for(int col=0;col<8;col++) {
            uint32_t color = (byte & (0x01 << col)) ? fg : bg;
            fb_fill_rect(x + col*scale, y + row*scale, scale, scale, color);
        }
    }
}

void fb_draw_str_scaled(int x,int y,const char *s,uint32_t fg,uint32_t bg,int scale) {
    int cx=x;
    while(*s) {
        if(*s=='\n'){cx=x;y+=8*scale+scale;}
        else{fb_draw_char_scaled(cx,y,*s,fg,bg,scale);cx+=9*scale;}
        s++;
    }
}

