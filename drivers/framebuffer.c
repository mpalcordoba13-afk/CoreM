/* Decodificador BMP mínimo                                             */
/* Soporta: BMP 24bpp y 32bpp, sin compresión (BI_RGB).               */
/* El BMP se almacena bottom-up, lo invertimos al dibujar.            */
/* Escala la imagen para que quepa en (max_w x max_h) manteniendo AR. */
/* ================================================================== */
void fb_draw_bmp(int dx, int dy, int max_w, int max_h,
                  const uint8_t *data, uint32_t data_len) {
    if (!data || data_len < 54) return;
    /* Cabecera BMP */
    if (data[0]!='B'||data[1]!='M') return;
    uint32_t pixel_off = (uint32_t)data[10]|((uint32_t)data[11]<<8)|
                         ((uint32_t)data[12]<<16)|((uint32_t)data[13]<<24);
    if (pixel_off >= data_len) return;
    /* DIB header (BITMAPINFOHEADER) en offset 14 */
    int32_t  img_w = (int32_t)((uint32_t)data[18]|((uint32_t)data[19]<<8)|
                                ((uint32_t)data[20]<<16)|((uint32_t)data[21]<<24));
    int32_t  img_h = (int32_t)((uint32_t)data[22]|((uint32_t)data[23]<<8)|
                                ((uint32_t)data[24]<<16)|((uint32_t)data[25]<<24));
    uint16_t bpp   = (uint16_t)(data[28]|((uint32_t)data[29]<<8));
    uint32_t compr = (uint32_t)data[30]|((uint32_t)data[31]<<8)|
                     ((uint32_t)data[32]<<16)|((uint32_t)data[33]<<24);
    if (compr != 0) return;      /* solo BI_RGB */
    if (bpp != 24 && bpp != 32) return;

    int flip = 1;                /* BMP bottom-up por defecto */
    if (img_h < 0) { img_h = -img_h; flip = 0; }
    if (img_w <= 0 || img_h <= 0) return;

    int bytes_pp = bpp / 8;
    /* Row stride de BMP: múltiplo de 4 bytes */
    uint32_t stride = ((uint32_t)(img_w * bytes_pp) + 3) & ~3u;

    /* Calcular escala para caber en max_w x max_h */
    int scale = 1;
    if (max_w > 0 && max_h > 0) {
        int sx = img_w / max_w;
        int sy = img_h / max_h;
        scale = sx > sy ? sx : sy;
        if (scale < 1) scale = 1;
    }

    int draw_w = img_w / scale;
    int draw_h = img_h / scale;

    for (int row = 0; row < draw_h; row++) {
        int src_row = flip ? (img_h - 1 - row * scale) : (row * scale);
        uint32_t row_off = pixel_off + (uint32_t)src_row * stride;
        for (int col = 0; col < draw_w; col++) {
            uint32_t poff = row_off + (uint32_t)(col * scale) * bytes_pp;
            if (poff + (uint32_t)bytes_pp > data_len) continue;
            uint8_t b = data[poff];
            uint8_t g = data[poff+1];
            uint8_t r = data[poff+2];
            /* fb_color espera R,G,B pero internamente almacena BGRX */
            fb_put_pixel(dx + col, dy + row, fb_color(r, g, b));
        }
    }
}
