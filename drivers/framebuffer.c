#include "framebuffer.h"
#include <stdint.h>
#include <stddef.h>

#define VGA_WIDTH  1280
#define VGA_HEIGHT 720

static uint32_t *fb_hw    = 0;
static uint32_t  fb_w     = 0;
static uint32_t  fb_h     = 0;
static uint32_t  fb_pitch = 0;
static uint32_t  backbuf[VGA_WIDTH * VGA_HEIGHT];

void fb_init(uint32_t *addr, uint32_t width, uint32_t height, uint32_t pitch) {
    fb_hw = addr;
    fb_w  = width  > VGA_WIDTH  ? VGA_WIDTH  : width;
    fb_h  = height > VGA_HEIGHT ? VGA_HEIGHT : height;
    fb_pitch = pitch;
}
uint32_t fb_width(void)  { return fb_w; }
uint32_t fb_height(void) { return fb_h; }

/* QEMU -vga std = formato BGRX en memoria */
uint32_t fb_color(uint8_t r, uint8_t g, uint8_t b) {
    return ((uint32_t)b << 16) | ((uint32_t)g << 8) | (uint32_t)r;
}

void fb_put_pixel(int x, int y, uint32_t color) {
    if ((uint32_t)x >= fb_w || (uint32_t)y >= fb_h) return;
    backbuf[y * fb_w + x] = color;
}

void fb_fill_rect(int x, int y, int w, int h, uint32_t color) {
    int x2 = x + w; if (x2 > (int)fb_w) x2 = fb_w;
    int y2 = y + h; if (y2 > (int)fb_h) y2 = fb_h;
    if (x < 0) x = 0;
    if (y < 0) y = 0;
    for (int row = y; row < y2; row++) {
        uint32_t *line = backbuf + row * fb_w;
        for (int col = x; col < x2; col++)
            line[col] = color;
    }
}

void fb_draw_rect(int x, int y, int w, int h, uint32_t color) {
    fb_fill_rect(x, y, w, 1, color);
    fb_fill_rect(x, y+h-1, w, 1, color);
    fb_fill_rect(x, y, 1, h, color);
    fb_fill_rect(x+w-1, y, 1, h, color);
}

void fb_draw_line(int x0,int y0,int x1,int y1,uint32_t color) {
    int dx=x1-x0; if(dx<0)dx=-dx;
    int dy=y1-y0; if(dy<0)dy=-dy;
    int sx=x0<x1?1:-1, sy=y0<y1?1:-1, err=dx-dy;
    while(1) {
        fb_put_pixel(x0,y0,color);
        if(x0==x1&&y0==y1) break;
        int e2=2*err;
        if(e2>-dy){err-=dy;x0+=sx;}
        if(e2< dx){err+=dx;y0+=sy;}
    }
}

void fb_draw_circle(int cx,int cy,int r,uint32_t color) {
    int x=r,y=0,err=0;
    while(x>=y) {
        fb_put_pixel(cx+x,cy+y,color); fb_put_pixel(cx+y,cy+x,color);
        fb_put_pixel(cx-y,cy+x,color); fb_put_pixel(cx-x,cy+y,color);
        fb_put_pixel(cx-x,cy-y,color); fb_put_pixel(cx-y,cy-x,color);
        fb_put_pixel(cx+y,cy-x,color); fb_put_pixel(cx+x,cy-y,color);
        y++; err+=1+2*y;
        if(2*(err-x)+1>0){x--;err+=1-2*x;}
    }
}

void fb_fill_circle(int cx,int cy,int r,uint32_t color) {
    for(int y=-r;y<=r;y++)
        for(int x=-r;x<=r;x++)
            if(x*x+y*y<=r*r)
                fb_put_pixel(cx+x,cy+y,color);
}

void fb_fill_gradient(int x,int y,int w,int h,uint32_t c1,uint32_t c2) {
    uint8_t r1=c1&0xFF, g1=(c1>>8)&0xFF, b1=(c1>>16)&0xFF;
    uint8_t r2=c2&0xFF, g2=(c2>>8)&0xFF, b2=(c2>>16)&0xFF;
    for(int row=0;row<h;row++) {
        uint8_t r=r1+(int)(r2-r1)*row/h;
        uint8_t g=g1+(int)(g2-g1)*row/h;
        uint8_t b=b1+(int)(b2-b1)*row/h;
        fb_fill_rect(x,y+row,w,1,fb_color(r,g,b));
    }
}

void fb_flush(void) {
    for(uint32_t y=0;y<fb_h;y++) {
        uint32_t *dst=(uint32_t*)((uint8_t*)fb_hw+y*fb_pitch);
        uint32_t *src=backbuf+y*fb_w;
        for(uint32_t x=0;x<fb_w;x++) dst[x]=src[x];
    }
}

